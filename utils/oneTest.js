function oneTest(n) {     // Define la función oneTest que toma un argumento n
    if (n % 3 === 0) {    // Si n es divisible por 3
      return true;        // Retorna true
    }
    return false;         // De lo contrario, retorna false
  }
  
  module.exports = oneTest; // Exporta la función oneTest
  
// función simple que verifica si un número es divisible por 3.
